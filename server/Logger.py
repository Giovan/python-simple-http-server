#!/usr/bin/env python
# -*- coding: utf-8 -*-


class Logger:
    """Logger"""

    def __init__(self):
        pass

    @staticmethod
    def debug(msg):
        print(msg)

    @staticmethod
    def info(msg):
        print(msg)

    @staticmethod
    def warn(msg):
        print(msg)

    @staticmethod
    def error(msg):
        print(msg)
